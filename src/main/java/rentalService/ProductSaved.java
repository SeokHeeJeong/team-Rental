package rentalService;

public class ProductSaved extends AbstractEvent {

    private Long id;
    private String name;

    public ProductSaved(){
        super();
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
